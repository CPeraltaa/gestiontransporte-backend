package com.usac.analisis2.managetransporte.model;

import java.io.Serializable;
import java.util.Collection;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.validation.constraints.Size;

/**
 *
 * @author carlospecam
 */
@Entity
@Table(name = "Flotilla")
@NamedQueries({
    @NamedQuery(name = "Flotilla.findAll", query = "SELECT f FROM Flotilla f"),
    @NamedQuery(name = "Flotilla.findById", query = "SELECT f FROM Flotilla f WHERE f.id = :id"),
    @NamedQuery(name = "Flotilla.findByNombre", query = "SELECT f FROM Flotilla f WHERE f.nombre = :nombre"),
    @NamedQuery(name = "Flotilla.findByDescripcion", query = "SELECT f FROM Flotilla f WHERE f.descripcion = :descripcion")})
public class Flotilla implements Serializable {

    @Size(max = 100)
    @Column(name = "nombre")
    private String nombre;
    @Size(max = 200)
    @Column(name = "descripcion")
    private String descripcion;
    @OneToMany(mappedBy = "flotillaVehiculo")
    private Collection<Vehiculo> vehiculoCollection;

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;

    public Flotilla() {
    }

    public Flotilla(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }


    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Flotilla)) {
            return false;
        }
        Flotilla other = (Flotilla) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.usac.analisis2.managetransporte.model.Flotilla[ id=" + id + " ]";
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public Collection<Vehiculo> getVehiculoCollection() {
        return vehiculoCollection;
    }

    public void setVehiculoCollection(Collection<Vehiculo> vehiculoCollection) {
        this.vehiculoCollection = vehiculoCollection;
    }
    
}
